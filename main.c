#include <stdio.h>
#include <ctype.h>

int main()
{
    // Data types
    int a = 5;
    float b = 3.1416;
    double c = 3.1415951498093;
    short d = 20;
    long e = 829034823;
    char g = 'A';

    printf("%d\n", a);
    printf("%f\n", b);
    printf("%lf\n", c);
    printf("%hd\n", d);
    printf("%ld\n", e);
    printf("%c\n", g);

}