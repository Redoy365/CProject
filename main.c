#include<stdio.h>

int main(){
    int a = 5;
    float b = 5.6;
    double c = 7.8;
    char C = 'M';
    long l = 23;
    short s = 2;

    printf("%d\n", a);  // print int 

    printf("%lf\n", b);    // print float

    printf("%f\n", c);      // print double

    printf("%c\n", C);      //  print char

    printf("%ld\n", l);     //  print long

    printf("%d\n", s);      //print short

    
}